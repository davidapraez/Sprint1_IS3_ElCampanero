package clases;

public class avionAL {
	String idavion,topoavion,capacidad, modelo, tipopropulsion,numeromotores, pesonominal,idaerolinea;

	public avionAL(String idavion, String topoavion, String capacidad, String modelo, String tipopropulsion,
			String numeromotores, String pesonominal, String idaerolinea) {
		this.idavion = idavion;
		this.topoavion = topoavion;
		this.capacidad = capacidad;
		this.modelo = modelo;
		this.tipopropulsion = tipopropulsion;
		this.numeromotores = numeromotores;
		this.pesonominal = pesonominal;
		this.idaerolinea = idaerolinea;
	}

	public String getIdavion() {
		return idavion;
	}

	public void setIdavion(String idavion) {
		this.idavion = idavion;
	}

	public String getTopoavion() {
		return topoavion;
	}

	public void setTopoavion(String topoavion) {
		this.topoavion = topoavion;
	}

	public String getCapacidad() {
		return capacidad;
	}

	public void setCapacidad(String capacidad) {
		this.capacidad = capacidad;
	}

	public String getModelo() {
		return modelo;
	}

	public void setModelo(String modelo) {
		this.modelo = modelo;
	}

	public String getTipopropulsion() {
		return tipopropulsion;
	}

	public void setTipopropulsion(String tipopropulsion) {
		this.tipopropulsion = tipopropulsion;
	}

	public String getNumeromotores() {
		return numeromotores;
	}

	public void setNumeromotores(String numeromotores) {
		this.numeromotores = numeromotores;
	}

	public String getPesonominal() {
		return pesonominal;
	}

	public void setPesonominal(String pesonominal) {
		this.pesonominal = pesonominal;
	}

	public String getIdaerolinea() {
		return idaerolinea;
	}

	public void setIdaerolinea(String idaerolinea) {
		this.idaerolinea = idaerolinea;
	}
	
	
	
	
	
	
}
